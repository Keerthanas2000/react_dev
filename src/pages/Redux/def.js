// Key Concepts in Redux (with simple definitions)
// Concept	Definition
// Store	      The central place where all the state of your app is stored.
// Action	A plain object that describes what happened.
// Reducer	A function that decides how the state should change based on the action.
// Dispatch	A method used to send actions to the store.


//  Redux with React: How They Work Together
// React Redux Term  	Definition
// Provider	            A special component from react-redux that makes the Redux store available to the entire app.
// connect	            A higher-order component (HOC) that connects a React component to the Redux store.
// useSelector	            A hook that allows you to read data from the Redux store in function components.
// useDispatch	            A hook to dispatch actions from a function component.